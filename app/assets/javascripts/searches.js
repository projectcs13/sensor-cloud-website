$(function() {
$( "#slider-range" ).slider({
      range: true,
      min: 0,
      max: 10,
      values: [ 0, 5 ],
      slide: function( event, ui ) {
        $( "#min_val" ).val(ui.values[ 0 ]);
        $( "#max_val" ).val(ui.values[ 1 ]);
      }
    });
$( "#slider-range" ).css("width","11em");
});